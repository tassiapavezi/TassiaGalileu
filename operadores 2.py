variavel_numero_1 = int(input ("digite o primeiro numero:"))
variavel_numero_2 = int(input ("digite o segundo numero:"))
print(f'a soma dos dois numeros e de: {variavel_numero_1 + variavel_numero_2}')

#realizando a operacaod e subitracao
print(f'a subitracao dos dois numeros e de: {variavel_numero_1 - variavel_numero_2}')

#Realizando a operacao de multiplicacao
print(f' a multiplicacao dos numeros e de: {variavel_numero_1 * variavel_numero_2}')

#criar variavel multiplicacao
multiplicacao = variavel_numero_1 * variavel_numero_2
print (multiplicacao) #imprimir o valor armazenado na multiplicacao

print (f'a multiplicacao dos dois numeros e de: {multiplicacao}')

#Criar uma variavel chamada exponencial resultado 
# atribua o valor entre variavel 1 e variavel 2
#Logo em seguida, imprima exponencial resultado utilizando o F-SyntaxWarning

exponencialresultado = variavel_numero_1 ** variavel_numero_2

print (f' o exponencial resultado da v1 exponecial v2: {exponencialresultado}')

#agora vou usar  o operador divisao
divisao = variavel_numero_1 / variavel_numero_2
print (f' o resultado da divisao de v1 exponecial v2: {divisao}')

#Em Python o % chama RESTO e nao faz o percentual
resto = variavel_numero_1 % variavel_numero_2
print (f' o resto da divisao de v1 com v2: {resto}')

